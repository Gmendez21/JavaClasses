package com.QuickReview_AllJava;

public class DataTypesAndVariables {

	public static void main(String[] args) {

		/*
		 * In Java, there are several data types which can be divided into two
		 * categories: primitive and non-primitive.
		 * 
		 * Primitive Data Types:
		 * 
		 * int: Integer type (whole numbers, positive or negative) long: Long Integer
		 * type (whole numbers, positive or negative) float: Floating-point type (real
		 * numbers) double: Double precision floating-point type (real numbers) boolean:
		 * Boolean type (true or false) char: Character type (single character)
		 * Non-Primitive Data Types:
		 * 
		 * String: A sequence of characters Array: A collection of similar data types
		 * Class: A blueprint for creating objects (user-defined data type) A variable
		 * is a named memory location that is used to store a value in a program. In
		 * Java, you must declare a variable before you can use it, and you need to
		 * specify its data type. Here's an example of declaring variables in Java:
		 */

		int age = 30;
		double height = 5.6;
		char initial = 'J';
		String name = "John Doe";

		System.out.println("Age: " + age);
		System.out.println("Height: " + height);
		System.out.println("Initial: " + initial);
		System.out.println("Name: " + name);
	}

	/*
	 * In the example, age, height, initial, and name are all variables, and the
	 * data type of each variable is specified on the right side of the assignment
	 * operator (=).
	 */

}
