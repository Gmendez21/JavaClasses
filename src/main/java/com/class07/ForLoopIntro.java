package com.class07;

public class ForLoopIntro {

	public static void main(String[] args) {
		
		for(int i=1; i<=3; i++) {
			
			System.out.println("Hello");
			
			//for (initialization; condition; increment/decrement) {
		   // code } 
		}
		for(int i=10; i>=1; i--) {
			System.out.print(i+" ");
		}

	}

}
